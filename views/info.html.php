      <h3>Funconf II</h3>
      <p>Funconf II, presented by <a href="http://twitter.com/paulca">Paul</a> &amp; <a href="http://twitter.com/eamonleonard">Eamo</a>, was held in <a href="http://www.lismorecastle.com">Lismore Castle</a>, between the 23rd and 25th September, 2011. 80 of the worlds finest developers and designers got together to have a weekend of discussions loosely based on the theme of &lsquo;old and new&rsquo;. It all kicked off with a 3 course meal prepared by <a href="http://en.wikipedia.org/wiki/Kevin_Thornton_(chef)">Kevin Thornton</a>. <a href="http://twitter.com/usiegj00">Jonathan Siegel</a> provided the foreword: reflecting on how much had changed in a year and he was fascinated how people could have life changing experiences within that time.</p>

      <p>Transport arrived in the form of a double deck tour bus, which gently conveyed the group to Lismore, Waterford, in the south of Ireland. In the castle stables, a whole <a href="http://www.ballymaloe.ie/">Ballymaloe</a> pig had been roasting since lunchtime. Pork and fresh salmon were served on trestle tables with hay bales for seating. Local beer and cider was partnered with fine sparkling wine before a session of Irish Traditional music  in An Teach Dearg, the Red House pub.</p>

      <p>After breakfast on the 24th, in the castle art gallery, <a href="http://twitter.com/mojombo">Tom Preston Werner</a> talked about the importance of accepting invitations, before introducing <a href="http://twitter.com/werner">Werner Vogels</a>, who lectured us on the power of epidemics in software architecture, the presentation that got him hired at Amazon.</p>

      <p>Following Werner was <a href="http://twitter.com/jwhelton">James Whelton</a>, who reminded us how much we can affect young people in ways more significant than we can possibly know. <a href="http://twitter.com/kneath">Kyle Neath</a> followed James with a dissertation on how to build oneself over time.</p>

      <p><a href="http://twitter.com/destraynor">Des Traynor</a> gave us insight into how cutting things out from his life helped make him happier. He was followed by <a href="http://twitter.com/seanbonner">Sean Bonner</a>, who talked about Safecast, a crowd-sourced attempt to map the world&#8217;s radiation levels and change the radiation detection industry, a project founded after the Japanese Earthquake in March 2011.</p>

      <p><a href="http://twitter.com/robinb">Robin Blandford</a> had us place our right hand on the shoulder of anyone who was changing the world for the better and warned us if a hand wasn&#8217;t on our shoulder, we need to think bigger (or maybe stand next to more people &mdash;Ed.).</p>

      <p>The morning session was completed by <a href="http://twitter.com/asymco">Horace Dediu</a>, who showed us how he wants to reinvent market analysis, using data in smart ways to gain insight into things otherwise unknown.</p>

      <p>A packed lunch in the garden preceded the afternoon sessions. This was a chance for everyone to speak, raising questions around business, diversity, the arts and our responsibilities as influential people.</p>

      <p>The entire group reconvened after attiring themselves &lsquo;Funconf Smart&rsquo;. <a href="http://twitter.com/voodootikigod">Chris Williams</a> appealed to everyone to solve an uncomfortable phenomenon that no-one likes: hate on the internet.</p>

      <p>Finally, <a href="http://twitter.com/rands">Michael Lopp</a>, aka Rands, spoke about how startups need three roles: a designer, an engineer and a dictator.</p>

      <p>Approximately 10 minutes after Michael&#8217;s talk, the group assembled in the castle courtyard, where Paul and Eamo burst through the main arch driving a 1981 DeLorean, chased by a clown.</p>

      <p>After the iconic car screeched to a halt in front of the gathered crowd, a donkey was dragged alongside the DeLorean and <a href="http://twitter.com/cyrilhelnwein">Cyril Helnwein</a> photographed groups in front of the scene. A llama had also been brought, but it ran away.</p>

      <p>After photos, the group assembled for dinner in the <a href="http://en.wikipedia.org/wiki/Augustus_Welby_Northmore_Pugin">Pugin</a> room, the castle banqueting hall. Paul and Eamon thanked everybody, and Tom Preston Werner talked for a few more minutes about the importance of crafting experiences.</p>

      <p>After dinner, the group was invited back to the main hall to listen Diarmuid Ó Drisceoil, a local <a href="http://en.wikipedia.org/wiki/Seancha%C3%AD">seanchaí</a>, tell his stories. This preceded a performance by <a href="http://www.kila.ie/">Kila</a> in the Pugin room that surely no one who was there will ever forget.</p>

      <p>The party continued within the castle late into the night, before bleary eyes reconvened in the morning for breakfast, a presentation about the history of the castle by estate manager Denis Nevin, and a bus trip back to Dublin. A barbecue at the <a href="http://odessa.ie/club">Odessa Club</a> closed the event.</p>

      <p>The organisers would like to thank Denis and Maura at Lismore Castle, without whose assistance and dedication this could not have happened. <a href="http://www.flickr.com/search/?q=funconf&amp;d=taken-20110901-&amp;ss=0&amp;ct=0&amp;mt=all&amp;w=all&amp;adv=1">Many photos</a> were taken, and much fun was had by all.</p>
      
